# name-->变量名
name = "老王*100000"
age = 40
print("老王")
print("name")
print(name, age)

# 多重赋值
mingzi, age, sex = "老王", 40, "男"
print(mingzi, age, sex)

# 链式赋值:把同一个值赋值给多个变量名
a = b = c = 5
print(a, b, c)

# 交叉赋值：将两个变量指向的值进行交换
m = 1
n = 2
print(m, n)
m, n = n, m
print(m, n)

